<script>
	window.onload = function() {
		taker(false, true);
	}

	var wizard = 0;
	var wLimit = 4;

	var takerr = function(nn, pp) {
		var n = document.getElementById('nextw');
		var p = document.getElementById('prevw');
		if (nn) {
			n.style.display = 'none';
		} else {
			n.style.display = '';
		}
		if (pp) {
			p.style.display = 'none';
		} else {
			n.style.display = '';
		}
	}

	var nextw = function() {
		if (wizard < 3) {
			wizard++;
		}

		var x = document.getElementsByClassName('step-pane');
		var y = (document.getElementsByClassName('steps'))[0].getElementsByTagName('li');
		var flag = false;
		for (var i = 0; i < wLimit; i++) {
			if (wizard == i) {
				x[i].classList.add('active')
				if (!flag) {
					y[i].classList.add('active');
					flag = !flag;
				}
			} else {
				x[i].classList.remove('active');
				if (!flag) {
					y[i].classList.add('active');
				}
			}
		}
	}

	var prevw = function() {
		if (wizard >= 1) {
			wizard--;
		}

		var x = document.getElementsByClassName('step-pane');
		var y = (document.getElementsByClassName('steps'))[0].getElementsByTagName('li');
		var flag = false;
		for (var i = 0; i < wLimit; i++) {
			if (wizard == i) {
				x[i].classList.add('active')
				if (!flag) {
					y[i].classList.add('active');
					flag = !flag;
				} else {
					y[i].classList.remove('active');
				}
			} else {
				x[i].classList.remove('active');
				if (!flag) {
					y[i].classList.add('active');
				} else {
					y[i].classList.remove('active');
				}
			}
		}
	}
